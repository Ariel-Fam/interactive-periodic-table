export const getCategoryColor = (category: string): string => {
  const colors: Record<string, string> = {
    "nonmetal": "bg-green-500/20 border-green-500",
    "noble-gas": "bg-cyan-500/20 border-cyan-500",
    "alkali-metal": "bg-red-500/20 border-red-500",
    "alkaline-earth-metal": "bg-orange-500/20 border-orange-500",
    "metalloid": "bg-yellow-500/20 border-yellow-500",
    "halogen": "bg-teal-500/20 border-teal-500",
    "post-transition-metal": "bg-blue-500/20 border-blue-500",
    "transition-metal": "bg-purple-500/20 border-purple-500",
    "lanthanide": "bg-pink-500/20 border-pink-500",
    "actinide": "bg-rose-500/20 border-rose-500"
  };
  return colors[category] || "bg-gray-500/20 border-gray-500";
};
